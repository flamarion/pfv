def _check_no_category_duplicates(bind) -> None:
    rows = bind.execute(
        sa.text(
            "SELECT org_id, slug, is_system, COUNT(*) AS n "
            "FROM categories "
            "WHERE slug IS NOT NULL "
            "GROUP BY org_id, slug, is_system "
            "HAVING COUNT(*) > 1"
        )
    ).all()
    if rows:
        rendered = ", ".join(
            f"(org={r.org_id}, slug={r.slug!r}, is_system={r.is_system}, x{r.n})"
            for r in rows
        )
        raise RuntimeError(
            "Cannot create UNIQUE(org_id, slug, is_system) on categories: "
            f"duplicates exist: {rendered}. Resolve them and re-run."
        )


def _index_exists(bind, table: str, name: str) -> bool:
    """True if a named index exists on ``table`` for the current dialect.

    Used to make the upgrade idempotent against a prior downgrade that
    re-created ``ix_categories_org_id`` to keep MySQL's FK index cover.
    """
    if bind.dialect.name == "mysql":
        row = bind.execute(
            sa.text(
                "SELECT 1 FROM information_schema.STATISTICS "
                "WHERE TABLE_SCHEMA = DATABASE() "
                "AND TABLE_NAME = :t AND INDEX_NAME = :n LIMIT 1"
            ),
            {"t": table, "n": name},
        ).first()
        return row is not None
    # SQLite uses sqlite_master for index catalog.
    row = bind.execute(
        sa.text(
            "SELECT 1 FROM sqlite_master "
            "WHERE type = 'index' AND tbl_name = :t AND name = :n LIMIT 1"
        ),
        {"t": table, "n": name},
    ).first()
    return row is not None


