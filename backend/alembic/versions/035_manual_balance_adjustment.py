"""Manual balance adjustment (Track E).

Revision ID: 035_manual_balance_adjustment
Revises: 034_unique_org_name
Create Date: 2026-05-08

Three additive changes:

1. ``organizations.allow_manual_balance_adjustment`` boolean (default 0).
   Org admins toggle this on to allow direct balance edits on accounts.
   Default OFF preserves the historical invariant that ``account.balance``
   is derived strictly from transactions.

2. ``transactions.is_manual_adjustment`` boolean (default 0). Marks the
   tx rows generated by the new ``POST /accounts/{id}/adjust-balance``
   endpoint so reporting filters can exclude them, transfer detection
   can ignore them, and bulk delete can skip them.

3. ``UNIQUE(org_id, slug, is_system)`` on ``categories``. The new lazy
   ``balance-adjustment`` system category is created on first use under
   a try/except IntegrityError race; the unique index is what makes
   that race-safe. Defensive precheck scans for any existing
   ``(org_id, slug, is_system)`` duplicates before adding the index so
   a polluted dataset bails with a clear error rather than crashing on
   the DDL.
"""
